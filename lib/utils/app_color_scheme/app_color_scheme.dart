import 'package:flutter/material.dart';

class AppColorScheme {
  static Color raisinBlack() {
    return const Color.fromRGBO(33, 30, 41, 1);
  }

  static Color darkPurple() {
    return const Color.fromRGBO(50, 45, 64, 1);
  }

  static Color purple() {
    return const Color.fromRGBO(111, 96, 190, 1);
  }
}
